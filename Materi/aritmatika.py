angka1 = 25
angka2 = 2

#Penjumlahan
hasil = angka1 + angka2

print(hasil)
print(angka1 + angka2)
print(f"{angka1} + {angka2} = {hasil}")

#Pengurangan
hasil = angka1 - angka2

print(hasil)
print(angka1 - angka2)
print(f"{angka1} - {angka2} = {hasil}")

#Perkalian
hasil = angka1 * angka2

print(hasil)
print(angka1 * angka2)
print(f"{angka1} * {angka2} = {hasil}")

#Pembagian
hasil = angka1 / angka2

print(hasil)
print(angka1 / angka2)
print(f"{angka1} / {angka2} = {hasil}")

#Modulus
hasil = angka1 % angka2

print(hasil)
print(angka1 % angka2)
print(f"{angka1} % {angka2} = {hasil}")

